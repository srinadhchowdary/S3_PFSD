#Relational or comparision operators
x=10
y=12
print("Relational operaters follows:")
print(x>y)
print(x<y)
print(x==y)
print(x>=20)
print(x<=15)

#LOGICAL OPERATORS:
print("LOGICAL OPERATORS FOLLOWS:")
x=True
y=False
print(x and y)
print(x or y)
print(not x)

#BITWISE OPERATORS:
print("BITWISE OPERATORS:")
x=10
y=4
print(x & y)
print(x | y)
print(~x)
print(x ^ y)
print(x >> 2)
print(x << 2)

